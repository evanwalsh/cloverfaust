<h3>Post formatting</h3>
<p>All posts on <?=$siteName?> are formatted with the Textile markup language.</p>
<p>Please note that only bold, italics, links, and images are allowed.</p>
<p>Here's how you do it:</p>
<ul>
	<li>Bold: <code>*Bold rocks!*</code> &raquo; <strong>Bold rocks!</strong></li>
	<li>Italic: <code>_Italic rocks!_</code> &raquo; <em>Italic rocks!</em></li>
	<li>Links: <code>"Test":http://example.com</code> &raquo; <a href="http://example.com">Test</a></li>
	<li>Image: <code>!http://g.photos.cx/star-c2.png!</code> &raquo; <img src="http://g.photos.cx/star-c2.png"/></li>
</ul>

